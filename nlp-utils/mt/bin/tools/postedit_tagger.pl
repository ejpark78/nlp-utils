use strict;
use Encode;
use FileHandle;
use Getopt::Long;
#====================================================================
my (%args) = (	
);

GetOptions(
	'es' => \$args{"es"},
	'pos' => \$args{"pos"},
);
#====================================================================
# MAIN
#====================================================================
main: {
	Postedit_spanish(\%args);	
}
#====================================================================
# sub functions
#====================================================================
sub Postedit_spanish {
	my (%param) = (%{shift(@_)});

	my (@buf) = ();
	while( my $line = <STDIN> ) {
		$line =~ s/\s+$//;
		
		my ($word, $pos, $root_form) = split(/\t/, $line, 3);
		
		$word .= "/$pos" if( $param{"pos"} );

		push(@buf, $word);
	}

	print join(" ", @buf)."\n\n";
}
