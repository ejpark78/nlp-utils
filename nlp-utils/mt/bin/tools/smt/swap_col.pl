#!/usr/bin/perl -w
#====================================================================
use strict;
use utf8;
use Getopt::Long;
#====================================================================
my (%args) = (
	"d" => "\t",
);

GetOptions(
	"d=s" => \$args{"d"},
);
#====================================================================
binmode(STDIN, ":utf8");
binmode(STDOUT, ":utf8");
#====================================================================
main: {
	while( my $line=<STDIN> ) {
		$line =~ s/^\s+|\s+$//g;

		my($a, $b) = split(/\t/, $line, 2);

		$a =~ s/^\s+|\s+$//g;
		$b =~ s/^\s+|\s+$//g;

		print "$b\t$a\n";
	}
}
#====================================================================
__END__
