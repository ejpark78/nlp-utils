use strict;
use Encode;
use utf8;
use JSON;
use Getopt::Long;
use Data::Printer;
#====================================================================
my (%args) = (
);

GetOptions(
	"debug" => \$args{"debug"}
);
#====================================================================
# MAIN
#====================================================================
main: {
	binmode(STDIN, "utf8");
	binmode(STDOUT, "utf8");
	binmode(STDERR, "utf8");

	my (@title) = ();

	my $json = JSON->new;
	while( my $line = <STDIN> ) {
		$line =~ s/\s+$//;

		next if( $line eq "" );

		my $decoded = $json->decode( $line );

		p($decoded), "\n";
	}
}
