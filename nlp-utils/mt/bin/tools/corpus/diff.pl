#!/usr/bin/perl
#====================================================================
use strict;
use Encode;
use FileHandle;
use File::Path;
use Getopt::Long;
#====================================================================
my (%args) = (  
);

GetOptions(
);
#====================================================================
# MAIN
#====================================================================
main: {
  my (%data_a) = ();
  my (%data_b) = ();

  while( my $line = <STDIN> ) {
    $line =~ s/\s+$//;

    my (@token) = split(/\t/, $line);

    $data_a{$token[0]."\t".$token[1]} = 1;
    $data_b{$token[2]."\t".$token[3]} = 1;
  }

  foreach my $k ( keys %data_a ) {
    next if( defined($data_b{$k}) );

    print "$k\n";
  }
}
