#!/usr/bin/perl -w
#====================================================================
use strict;
use utf8;
use Encode;
use Getopt::Long;
use BerkeleyDB; 
#====================================================================
my (%args) = ();
GetOptions();
#====================================================================
binmode(STDIN, ":utf8");
binmode(STDOUT, ":utf8");
binmode(STDERR, ":utf8");
#====================================================================
main: {
  	while( my $line = <STDIN> ) {
		$line =~ s/\s+$//;
		
		my ($k, $v) = split(/\t/, $line, 2);

		print "$v\t$k\n";
	}

}
#====================================================================
sub comma {
	my $cnt = shift(@_);

	$cnt = reverse join ",", (reverse $cnt) =~ /(\d{1,3})/g;

	return $cnt;
}
#====================================================================
__END__      

