#!/usr/bin/perl -w
#====================================================================
use strict;
use utf8;
use Encode;
use Getopt::Long;
#====================================================================
my (%args) = (
);

GetOptions(
	'empty' => \$args{"empty"}
);
#====================================================================
binmode(STDIN, ":utf8");
binmode(STDOUT, ":utf8");
#====================================================================
main: {
	while( <STDIN> ) {
		s/\s+$//g;
		s/\/[^ ]+//g;

		if( $args{"empty"} && $_ ne "" ) {
			print "$_\n";
		} else {
			print "$_\n";
		}
	}
}

#====================================================================
__END__
