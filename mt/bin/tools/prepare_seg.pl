#!/usr/bin/perl -w
#====================================================================
use strict;
use utf8;
use Encode;
#====================================================================
binmode(STDIN, ":utf8");
binmode(STDOUT, ":utf8");
#====================================================================
main: {

	while( my $line = <STDIN> ) {
		$line =~ s/\s+$//;
		
		my $s = "B";
		foreach my $w ( split(//, $line) ) {			
			if( $w ne " " ) {
				print "$w $s .\n";
			}

			if( $w eq " " ) {
				$s = "B";
			} else {
				$s = "I";
			}
		}
		
		print "\n";
	}	
		
	print ". . .\n";
}

#====================================================================
__END__